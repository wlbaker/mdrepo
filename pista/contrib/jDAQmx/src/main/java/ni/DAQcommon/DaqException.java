package ni.DAQcommon;

public class DaqException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 371305413457786562L;

	public DaqException() {
		super();
	}

	public DaqException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public DaqException(String arg0) {
		super(arg0);
	}

	public DaqException(Throwable arg0) {
		super(arg0);
	}

}
